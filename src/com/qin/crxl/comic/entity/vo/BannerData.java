package com.qin.crxl.comic.entity.vo;

public class BannerData extends BaseEntityVO {
	private String id; //banner图id
	private String comicUrl; //图片外链地址
	private String httpImg; //图片地址
	private String  state; //图片状态
	private String  click; //图片点击量
	private String title; //图片标题
	private String implDate; //图片操作时间
	private String cartoonId; //漫画Id
	private String iosState;    //ios漫画上架状态状态
	private String anroidState;    //android漫画上架状态状态
	
	
	public String getIosState() {
		return iosState;
	}
	public void setIosState(String iosState) {
		this.iosState = iosState;
	}
	public String getAnroidState() {
		return anroidState;
	}
	public void setAnroidState(String anroidState) {
		this.anroidState = anroidState;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getComicUrl() {
		return comicUrl;
	}
	public void setComicUrl(String comicUrl) {
		this.comicUrl = comicUrl;
	}
	public String getHttpImg() {
		return httpImg;
	}
	public void setHttpImg(String httpImg) {
		this.httpImg = httpImg;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getClick() {
		return click;
	}
	public void setClick(String click) {
		this.click = click;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getImplDate() {
		return implDate;
	}
	public void setImplDate(String implDate) {
		this.implDate = implDate;
	}
	public String getCartoonId() {
		return cartoonId;
	}
	public void setCartoonId(String cartoonId) {
		this.cartoonId = cartoonId;
	}
	
}
